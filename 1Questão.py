# Numeros Inteiros
num1 = int(input("Insira o primeiro número: "))
num2 = int(input("Insira o segundo número: "))
num3 = int(input("Insira o terceiro número: "))

# Listar
numeros = [num1, num2, num3]

# Ordenar
numeros.sort()

# Imprimir
print("Números em ordem crescente:")
for numero in numeros:
    print(numeros)
